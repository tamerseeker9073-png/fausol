export function searchCiudades(query: string, max = 12): Ciudad[] {
  const q = query.toLowerCase().trim()
  if (!q) return []
  return CIUDADES.filter(
    (c) => c.nombre.toLowerCase().includes(q) || c.provincia.toLowerCase().includes(q)
  ).slice(0, max)
}

export function getCiudad(id: string): Ciudad | undefined {
  return CIUDADES.find((c) => c.id === id)
}
